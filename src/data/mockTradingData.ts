/** Generate candlestick chart data */
export function generateCandlestickData(
  count: number = 200, 
  intervalMs: number = 300000, // 5 minutes
  basePrice: number = 500
): CandleData[] {
  const data: CandleData[] = [];
  let currentPrice = basePrice;
  const now = Date.now();
  
  for (let i = count - 1; i >= 0; i--) {
    const timestamp = now - (i * intervalMs);
    const volatility = currentPrice * 0.02; // 2% volatility
    
    const open = currentPrice;
    const change = (Math.random() - 0.5) * volatility;
    const close = open + change;
    const high = Math.max(open, close) + Math.random() * volatility * 0.5;
    const low = Math.min(open, close) - Math.random() * volatility * 0.5;
    const volume = Math.random() * 10000 + 1000;
    
    data.push({
      timestamp,
      open: parseFloat(open.toFixed(2)),
      high: parseFloat(high.toFixed(2)),
      low: parseFloat(low.toFixed(2)),
      close: parseFloat(close.toFixed(2)),
      volume: parseFloat(volume.toFixed(2)),
    });
    
    currentPrice = close;
  }
  
  return data;
}

/** Generate order book data */
export function generateOrderBook(currentPrice: number = 100): OrderBook {
  const bids: OrderBookItem[] = [];
  const asks: OrderBookItem[] = [];
  
  let bidTotal = 0;
  let askTotal = 0;
  
  for (let i = 0; i < 15; i++) {
    const bidPrice = currentPrice * (1 - (i + 1) * 0.001);
    const askPrice = currentPrice * (1 + (i + 1) * 0.001);
    const bidAmount = Math.random() * 10 + 1;
    const askAmount = Math.random() * 10 + 1;
    
    bidTotal += bidAmount;
    askTotal += askAmount;
    
    bids.push({
      price: parseFloat(bidPrice.toFixed(2)),
      amount: parseFloat(bidAmount.toFixed(5)),
      total: parseFloat(bidTotal.toFixed(2)),
    });
    
    asks.push({
      price: parseFloat(askPrice.toFixed(2)),
      amount: parseFloat(askAmount.toFixed(5)),
      total: parseFloat(askTotal.toFixed(2)),
    });
  }
  
  return { bids, asks };
}

/** Get a market by symbol */
export function getMarketBySymbol(symbol: string): Market | undefined {
  return mockMarkets.find(m => m.symbol === symbol);
}

/** Filter markets by type */
export function getMarketsByType(type: Market['type']): Market[] {
  return mockMarkets.filter(m => m.type === type);
}
